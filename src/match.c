int	match(char *str, char *ptr)
{
	int	i;

	i = 0;
	while (str[i] != '\0' && ptr[i] != '\0')
	{
		if (str[i] != ptr[i])
			return (1);
		i++;
	}
	if (str[i] != ptr[i])
		return (1);
	return (0);
}
